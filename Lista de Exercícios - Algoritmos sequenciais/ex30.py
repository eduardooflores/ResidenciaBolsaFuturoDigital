# ENTRADA
numero1 = 10.5
numero2 = 5.6
numero3 = 110.5
numero4 = 1.5
numero5 = 20.5
numero6 = 30.5

# PROCESSAMENTO
soma = numero1 + numero2 + numero3 + numero4 + numero5 + numero6
subtracao = numero1 - numero2 - numero3 - numero4 - numero5 - numero6

# SAÍDA
print(f"A soma dos valores é: {soma}")
print(f"A subtração dos valores é: {subtracao}")