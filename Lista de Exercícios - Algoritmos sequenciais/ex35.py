# ENTRADA
base = 20
altura = 30

# PROCESSAMENTO
perimetro = base * 3 # Para calcular um perímetro de um triangulo equilátero basta somar os seus lados
area = (base * altura) / 2 # Para calcular a area de um triangulo equilátero basta multiplicar a base pela altura e depois dividir por dois
diagonal = None

# SAÍDA
print(f"O perímetro do triangulo é de: {perimetro}")
print(f"A área do triangulo é de: {area}")
print(f"Um triangulo não possuí diagonal, portando o resultado é: {diagonal}, ou seja, nada.")