# Questão 33. Elabore um algoritmo que lê dois números e mostre os seguintes resultados:
# Dividendo, Divisor, Quociente, Resto.

# ENTRADA
numero = 10
segundo_numero = 20

# PROCESSAMENTO
quociente = numero // segundo_numero
resto = numero % segundo_numero

# SAÍDA
print(f"O dividendo é {numero}")
print(f"O divisor é {segundo_numero}")
print(f"O quociente é {quociente}")
print(f"O resto é {resto}")