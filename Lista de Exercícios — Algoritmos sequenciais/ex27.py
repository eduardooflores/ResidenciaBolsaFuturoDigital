# Questão 27. Lê um número e mostra seu sucessor e seu antecessor na tela.

# ENTRADA
numero = 10

# PROCESSAMENTO
numero_sucessor = numero + 1
numero_antecessor = numero - 1

# SAÍDA
print(f"O sucessor do número {numero} é {numero_sucessor}")
print(f"O antecessor do número {numero} é {numero_antecessor}")