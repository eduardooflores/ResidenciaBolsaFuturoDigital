# Questão 32. Elabore um algoritmo que leia uma temperatura em graus centígrados e mostre-a
# convertida em graus Fahrenheit. A fórmula de conversão é: F = (9 * C + 160) / 5 onde F é a
# temperatura em Fahrenheit e C é a temperatura em centígrados.

# ENTRADA
temperatura_c = 20

# PROCESSAMENTO
fahrenheit = (9 * temperatura_c + 160) / 5

# SAÍDA
print(f"A temperatura de {temperatura_c} C° em Fahrenheit é {fahrenheit} F°")